package Model;

public class TV {
    private String ip;
    private String code;

    public TV() {
        this.ip = ip;
        this.code = code;
    }

    
    // геттеры и сеттеры для полей ip и code
    public String getIp() {
        return ip;
    }
    
    public void setIp(String ip) {
        this.ip = ip;
    }
    
    public String getCode() {
        return code;
    }
    
    public void setCode(String code) {
        this.code = code;
    }
}
